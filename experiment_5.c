//5.Program demonstrating data types and format specifiers



#include <stdio.h>

int main() {
    int a = 10;
    float b = 12.5;
    double c = 123.456;
    char d = 'A';

    printf("Integer value: %d\n", a);
    printf("Float value: %f\n", b);
    printf("Double value: %lf\n", c);
    printf("Character value: %c\n", d);

    return 0;
}